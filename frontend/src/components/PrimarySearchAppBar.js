class PrimarySearchAppBar extends React.Component {
  state = {
    anchorEl: null,
    mobileMoreAnchorEl: null,
  };

  handleProfileMenuOpen = event => {
    this.setState({ anchorEl: event.currentTarget });
  };

  handleMenuClose = () => {
    this.setState({ anchorEl: null });
  };


  render() {
    const { anchorEl, mobileMoreAnchorEl } = this.state;
    const { classes, logOut, currentCouple } = this.props;
    const isMenuOpen = Boolean(anchorEl);
    const renderMenu = (
      <Menu
        anchorEl={anchorEl}
        anchorOrigin={{ vertical: 'top', horizontal: 'right' }}
        transformOrigin={{ vertical: 'top', horizontal: 'right' }}
        open={isMenuOpen}
        onClose={this.handleMenuClose}
      >
        <MenuItem
          component={Link} to="/guests"
          onClick={() => {this.handleMenuClose()}}>Guest List</MenuItem>

        <MenuItem
          component={Link} to="/registry"
          onClick={() => {this.handleMenuClose()}}>Our Wedding Registry</MenuItem>

        <MenuItem
          component={Link} to="/"
          onClick={() => {logOut(currentCouple)}}>Log Out</MenuItem>
      </Menu>
    );

    return (
      <div className={classes.root}>
        <AppBar position="static" style={{ backgroundColor: '#B84C65' }}>
          <Toolbar>
            <Typography className={classes.title} variant="h6" color="inherit" noWrap>
              <Link onClick to={ItemCollectionNestedGrid}>
                <img src={HtchedLogoDark}  style={{width: '200px'}}/>
              </Link>
            </Typography>
            <div className={classes.grow} />
            <div className={classes.search}>
              <div className={classes.searchIcon}>
                <SearchIcon />
              </div>
              <InputBase
                placeholder="Search Giftsâ€¦"
                classes={{
                  root: classes.inputRoot,
                  input: classes.inputInput,
                }}
                onChange={this.props.onSearchChange}
                showNoResults={false}
                value={this.props.searchTerm}
              />
            </div>
            <div className={classes.sectionDesktop}>
              <IconButton
                aria-owns={isMenuOpen ? 'material-appbar' : undefined}
                aria-haspopup="true"
                onClick={this.handleProfileMenuOpen}
                color="inherit"
              >
                <AccountCircle />
              </IconButton>
            </div>
          </Toolbar>
        </AppBar>
        {renderMenu}
      </div>
    );
  }
}

